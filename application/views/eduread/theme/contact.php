<script>
	$("#submit").click(function(e){
		// alert('dfdf');
		// debugger;
		e.preventDefault();
		let name = $("#name").val().trim();
		let email = $("#email").val().trim();
		let subject = $("#subject").val().trim();
		let comments = $("#comments").val().trim();
		if(name == "" || email == "" || subject == "" || comments == ""){
			alert("All field are required");
			$("#name").focus();
		}else{
			// validate email
			var mailformat = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/;
			if(email.match(mailformat)) { 
				formdata = $("form#contactform").serialize();
				$.post(baseUrl + "clients/contactus", formdata).done(function(data){
					if(data == 1){
						alert("Message successfully sent");
						clearFields();
					}else if(data == 0){
						alert("Unable to send message");
					}else{
						alert("Error occured, please contact the system adminstrator");
					}
				});					
			}else{
				alert("Email format is required");	
				$("#email").focus();						
			} 	
		}
	});

	function clearFields(){
		$("#name").val('');
		$("#email").val('');
		$("#subject").val('');
		$("#comments").val('');
		$("#name").focus();
	}	
</script>
